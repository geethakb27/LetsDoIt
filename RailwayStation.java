package tech;

public class RailwayStation {
	static String stationName;
	static String Address;
	static String TickatCounter;
	public static String getStationName() {
		return stationName;
	}
	public static void setStationName(String stationName) {
		RailwayStation.stationName = stationName;
	}
	public static String getAddress() {
		return Address;
	}
	public static void setAddress(String address) {
		Address = address;
	}
	public static String getTickatCounter() {
		return TickatCounter;
	}
	public static void setTickatCounter(String tickatCounter) {
		TickatCounter = tickatCounter;
	}


}
